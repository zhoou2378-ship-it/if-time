const availableYears = Object.keys(eraData)
  .map(Number)
  .sort((a, b) => a - b);

export function getNearestEra(targetYear) {
  const minYear = availableYears[0];
  const maxYear = availableYears[availableYears.length - 1];
  const safeYear = Math.min(Math.max(Number(targetYear), minYear), maxYear);

  return {
    safeYear,
    era: eraData[safeYear],
    isFallback: safeYear !== Number(targetYear),
  };
}
